uint32_t
sl_unicode_tolower(uint32_t c)
{
    int left = 0;
    int right = (sizeof(mappings) / sizeof(mappings[0])) - 2;
    while(left <= right) {
        int mid = (left + right) / 2;
        if(mappings[mid].upper == c) {
            return mappings[mid].lower;
        }
        if(mappings[mid].upper < c) {
            left = mid + 1;
        }
        if(mappings[mid].upper > c) {
            right = mid - 1;
        }
    }
    return c;
}

uint32_t
sl_unicode_toupper(uint32_t c)
{
    int left = 0;
    int right = (sizeof(mappings) / sizeof(mappings[0])) - 2;
    while(left <= right) {
        int mid = (left + right) / 2;
        if(mappings[mid].lower == c) {
            return mappings[mid].upper;
        }
        if(mappings[mid].lower < c) {
            left = mid + 1;
        }
        if(mappings[mid].lower > c) {
            right = mid - 1;
        }
    }
    return c;
}

